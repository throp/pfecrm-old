package com.pfe.crm.domain.entity;

import java.io.Serializable;

import com.pfe.crm.domain.type.FireExtinguisherServiceType;
import com.pfe.crm.domain.type.FireExtinguisherTestType;
import com.pfe.crm.domain.type.Floor;
import com.pfe.crm.domain.type.Year;

/**
 * FireExtinguisher.java
 * 
 * Represents an actual fire extinguisher at a given location.
 * 
 * Copyright (c) Northrop Development Group 2006
 * 
 * @author Ben Date: Aug 8, 2006
 */
public class FireExtinguisher extends InventoryItem implements Serializable
{
    // DATA MEMBERS

    private Manufacturer manufacturer;

    private Chemical chemical;

    private Weight weight;

    private Year year;

    private Floor floor;

    private String location;

    private boolean isLocked;

    private Year testYear;

    private FireExtinguisherTestType testType;

    private FireExtinguisherServiceType serviceType;

    private static final long serialVersionUID = -5992408992614483079L;

    /**
     * @return Returns the chemical.
     */
    public Chemical getChemical()
    {
        return chemical;
    }

    /**
     * @param chemical
     *            The chemical to set.
     */
    public void setChemical(Chemical chemical)
    {
        this.chemical = chemical;
    }

    /**
     * @return Returns the floor.
     */
    public Floor getFloor()
    {
        return floor;
    }

    /**
     * @param floor
     *            The floor to set.
     */
    public void setFloor(Floor floor)
    {
        this.floor = floor;
    }

    /**
     * @return Returns the location.
     */
    public String getLocation()
    {
        return location;
    }

    /**
     * @param location
     *            The location to set.
     */
    public void setLocation(String location)
    {
        this.location = location;
    }

    /**
     * @return Returns the manufacturer.
     */
    public Manufacturer getManufacturer()
    {
        return manufacturer;
    }

    /**
     * @param manufacturer
     *            The manufacturer to set.
     */
    public void setManufacturer(Manufacturer manufacturer)
    {
        this.manufacturer = manufacturer;
    }

    /**
     * @return Returns the year.
     */
    public Year getYear()
    {
        return year;
    }

    /**
     * @param year
     *            The year to set.
     */
    public void setYear(Year manufacturerYear)
    {
        this.year = manufacturerYear;
    }

    /**
     * @return Returns the weight.
     */
    public Weight getWeight()
    {
        return weight;
    }

    /**
     * @param weight
     *            The weight to set.
     */
    public void setWeight(Weight weight)
    {
        this.weight = weight;
    }

    /**
     * @return Returns the isLocked.
     */
    public boolean isLocked()
    {
        return isLocked;
    }

    /**
     * @param isLocked
     *            The isLocked to set.
     */
    public void setLocked(boolean isLocked)
    {
        this.isLocked = isLocked;
    }

    /**
     * @return Returns the isLocked.
     */
    public boolean getIsLocked()
    {
        return isLocked;
    }

    /**
     * @param isLocked
     *            The isLocked to set.
     */
    public void setIsLocked(boolean isLocked)
    {
        this.isLocked = isLocked;
    }

    /**
     * @return the serviceType
     */
    public FireExtinguisherServiceType getServiceType()
    {
        return serviceType;
    }

    /**
     * @param serviceType
     *            the serviceType to set
     */
    public void setServiceType(FireExtinguisherServiceType serviceType)
    {
        this.serviceType = serviceType;
    }

    /**
     * @return the testType
     */
    public FireExtinguisherTestType getTestType()
    {
        return testType;
    }

    /**
     * @param testType
     *            the testType to set
     */
    public void setTestType(FireExtinguisherTestType testType)
    {
        this.testType = testType;
    }

    /**
     * @return the testYear
     */
    public Year getTestYear()
    {
        return testYear;
    }

    /**
     * @param testYear
     *            the testYear to set
     */
    public void setTestYear(Year testYear)
    {
        this.testYear = testYear;
    }
}
