package com.pfe.crm.domain.entity;

import com.pfe.crm.core.domain.BaseEntity;

/**
 * Manufacturer.java
 * 
 * Represents a company which manufacturers anything (fire extinguisher,
 * suppression system, etc).
 * 
 * Copyright (c) Northrop Development Group 2006
 * 
 * @author Ben Date: Aug 3, 2006
 */
public class Manufacturer extends BaseEntity
{

    // DATA MEMBERS

    private String name;
    
    private String abbreviation;

    private Boolean isActive = Boolean.TRUE;

    private boolean isFireExtinguisher;

    private static final long serialVersionUID = 8771462249492122906L;

    /**
     * A string representation
     */
    public String toString()
    {
        return name;
    }

    /**
     * @return Returns the isFireExtinguisher.
     */
    public boolean getFireExtinguisher()
    {
        return isFireExtinguisher;
    }

    /**
     * @param isFireExtinguisher
     *            The isFireExtinguisher to set.
     */
    public void setIsFireExtinguisher(boolean isFireExtinguisher)
    {
        this.isFireExtinguisher = isFireExtinguisher;
    }

    /**
     * @return Returns the isFireExtinguisher.
     */
    public boolean getIsFireExtinguisher()
    {
        return isFireExtinguisher;
    }

    /**
     * @param isFireExtinguisher
     *            The isFireExtinguisher to set.
     */
    public void setFireExtinguisher(boolean isFireExtinguisher)
    {
        this.isFireExtinguisher = isFireExtinguisher;
    }

    /**
     * @return Returns the name.
     */
    public String getName()
    {
        return name;
    }

    /**
     * @param name
     *            The name to set.
     */
    public void setName(String name)
    {
        this.name = name;
    }

    /**
     * @return the isActive
     */
    public Boolean getIsActive()
    {
        return isActive;
    }

    /**
     * @param isActive
     *            the isActive to set
     */
    public void setIsActive(Boolean isActive)
    {
        this.isActive = isActive;
    }

    /**
     * @return the abbreviation
     */
    public String getAbbreviation()
    {
        return abbreviation;
    }

    /**
     * @param abbreviation the abbreviation to set
     */
    public void setAbbreviation(String abbreviation)
    {
        this.abbreviation = abbreviation;
    }

}
