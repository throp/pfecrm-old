package com.pfe.crm.core.domain;

import java.io.Serializable;

/**
 * BaseEntity.java
 * 
 * Base entity class from which all other entities should extend.
 * 
 * @author Ben Northrop
 * May 20, 2007
 *
 */
public class BaseEntity implements Serializable
{
    /**
     * Identifier for database - surrogate primary key.
     */
    private Integer id;

    /**
     * @return the id
     */
    public Integer getId()
    {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id)
    {
        this.id = id;
    }
    
    
}
