package com.pfe.crm.core.exception;

/**
 * CRMException
 * 
 * A generic runtime exception to indicate an exception in the CRM system.
 * 
 * @author Ben Northrop
 * 
 */
public class CRMException extends RuntimeException {

	/**
	 * Generated serial version id.
	 */
	private static final long serialVersionUID = -322003342236421887L;

	/**
	 * Default constructor
	 */
	public CRMException(String message, Throwable cause) {
		super(message, cause);
	}
}
